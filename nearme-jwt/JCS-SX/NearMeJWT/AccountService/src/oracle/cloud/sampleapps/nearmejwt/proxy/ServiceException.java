import javax.xml.ws.WebFault;

import oracle.cloud.sampleapps.nearmejwt.types.ServiceErrorMessage;

@WebFault(faultBean="oracle.cloud.sampleapps.nearmejwt.types.ServiceErrorMessage",
  targetNamespace="http://xmlns.oracle.com/adf/svc/errors/", name="ServiceErrorMessage")
public class ServiceException
  extends Exception
{
  private ServiceErrorMessage faultInfo;

  public ServiceException(String message, ServiceErrorMessage faultInfo)
  {
    super(message);
    this.faultInfo = faultInfo;
  }

  public ServiceException(String message, ServiceErrorMessage faultInfo,
                          Throwable t)
  {
    super(message,t);
    this.faultInfo = faultInfo;
  }

  public ServiceErrorMessage getFaultInfo()
  {
    return faultInfo;
  }

  public void setFaultInfo(ServiceErrorMessage faultInfo)
  {
    this.faultInfo = faultInfo;
  }
}
